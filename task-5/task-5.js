// 1. Створити змінну для зберігання числа з плаваючою комою.
// 2. Використати метод Math.floor() для округлення числа до меншого.
// 3. Вивести результат в консоль.

// Створення змінної для зберігання числа з плаваючою комою
let floatingNumber = 7.89;

// Використання Math.floor() для округлення числа до меншого цілого
let roundedNumber = Math.floor(floatingNumber);

// Виведення результату в консоль
console.log("Округлене число: " + roundedNumber);
