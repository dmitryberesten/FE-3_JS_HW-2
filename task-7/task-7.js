// 1. Створити змінну для зберігання рядка, який містить ціле число.
// 2. Використати метод parseInt() для перетворення рядка у ціле число.
// 3. Вивести результат в консоль.

// Створення змінної для зберігання рядка, який містить ціле число
let integerString = "123";

// Використання parseInt() для перетворення рядка у ціле число
let integerNumber = parseInt(integerString);

// Виведення результату в консоль
console.log("Перетворене ціле число: " + integerNumber);
