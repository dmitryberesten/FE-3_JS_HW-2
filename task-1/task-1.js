// 1. Створити змінні для зберігання температури за Цельсієм та Фаренгейтом.
// 2. Перевести температуру з Цельсія у Фаренгейти за допомогою формули.
// 3. Вивести результат в консоль.

// Створення змінних для зберігання температури за Цельсієм та Фаренгейтом
let celsius = 25;
let fahrenheit;

// Переведення температури з Цельсія у Фаренгейти
fahrenheit = celsius * (9 / 5) + 32;

// Виведення результату в консоль
console.log(celsius + "°C is " + fahrenheit + "°F");
