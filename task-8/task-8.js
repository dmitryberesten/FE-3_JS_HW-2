// 1. Створити змінну для зберігання числа.
// 2. Використати метод Math.sqrt() для обчислення квадратного кореня числа.
// 3. Вивести результат в консоль.

// Створення змінної для зберігання числа
let number = 64; 

// Використання Math.sqrt() для обчислення квадратного кореня числа
let squareRoot = Math.sqrt(number);

// Виведення результату в консоль
console.log("Квадратний корінь з " + number + " дорівнює: " + squareRoot);

